// booleans & comparison operator 

// booleans : true, false values

let num1 = 7;
let num2 = "4";

console.log(num1>num2); //false

// == vs === :
console.log(num1 == num2); // == : checks value not datatype

console.log(num1 === num2); // ==== : checks datatype

// != vs !==
console.log(num1 != num2); //checks if num1 and num2 are diff, if same then false and if different then true

console.log(num1 !== num2); //checks if type of num1 and num2 are diff, if same then false and if diff then true